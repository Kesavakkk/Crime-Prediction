from reportlab.lib.pagesizes import letter, A4
from reportlab.lib import colors
from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
from reportlab.platypus import SimpleDocTemplate, Table, TableStyle, Paragraph, Spacer, PageBreak
from reportlab.lib.units import inch
from datetime import datetime
import io

class CrimeReportGenerator:
    def __init__(self):
        self.styles = getSampleStyleSheet()
        
    def generate_prediction_report(self, results, crime_type, username):
        """Generate PDF report for crime predictions"""
        buffer = io.BytesIO()
        doc = SimpleDocTemplate(buffer, pagesize=A4)
        elements = []
        
        # Title
        title_style = ParagraphStyle('CustomTitle', parent=self.styles['Heading1'], fontSize=24, textColor=colors.HexColor('#667eea'), spaceAfter=30)
        elements.append(Paragraph("Crime Hotspot Prediction Report", title_style))
        elements.append(Spacer(1, 12))
        
        # Metadata
        meta_data = [
            ['Report Generated:', datetime.now().strftime('%Y-%m-%d %H:%M:%S')],
            ['Generated By:', username],
            ['Crime Type:', crime_type],
            ['Total States Analyzed:', str(len(results))]
        ]
        meta_table = Table(meta_data, colWidths=[2*inch, 4*inch])
        meta_table.setStyle(TableStyle([
            ('BACKGROUND', (0, 0), (0, -1), colors.HexColor('#f0f0f0')),
            ('TEXTCOLOR', (0, 0), (-1, -1), colors.black),
            ('ALIGN', (0, 0), (-1, -1), 'LEFT'),
            ('FONTNAME', (0, 0), (0, -1), 'Helvetica-Bold'),
            ('FONTSIZE', (0, 0), (-1, -1), 10),
            ('BOTTOMPADDING', (0, 0), (-1, -1), 12),
            ('GRID', (0, 0), (-1, -1), 1, colors.grey)
        ]))
        elements.append(meta_table)
        elements.append(Spacer(1, 20))
        
        # Summary
        safe_count = sum(1 for r in results if r[1] == 'SAFE')
        unsafe_count = len(results) - safe_count
        
        summary_data = [
            ['Status', 'Count', 'Percentage'],
            ['SAFE', str(safe_count), f'{(safe_count/len(results)*100):.1f}%'],
            ['UNSAFE', str(unsafe_count), f'{(unsafe_count/len(results)*100):.1f}%']
        ]
        summary_table = Table(summary_data, colWidths=[2*inch, 1.5*inch, 1.5*inch])
        summary_table.setStyle(TableStyle([
            ('BACKGROUND', (0, 0), (-1, 0), colors.HexColor('#667eea')),
            ('TEXTCOLOR', (0, 0), (-1, 0), colors.whitesmoke),
            ('ALIGN', (0, 0), (-1, -1), 'CENTER'),
            ('FONTNAME', (0, 0), (-1, 0), 'Helvetica-Bold'),
            ('FONTSIZE', (0, 0), (-1, 0), 12),
            ('BOTTOMPADDING', (0, 0), (-1, 0), 12),
            ('BACKGROUND', (0, 1), (-1, -1), colors.beige),
            ('GRID', (0, 0), (-1, -1), 1, colors.black)
        ]))
        elements.append(Paragraph("Summary", self.styles['Heading2']))
        elements.append(Spacer(1, 12))
        elements.append(summary_table)
        elements.append(Spacer(1, 20))
        
        # Detailed Results
        elements.append(Paragraph("Detailed State-wise Analysis", self.styles['Heading2']))
        elements.append(Spacer(1, 12))
        
        result_data = [['Rank', 'State/UT', 'Status']]
        for i, r in enumerate(results, 1):
            result_data.append([str(i), r[0], r[1]])
        
        result_table = Table(result_data, colWidths=[1*inch, 3*inch, 1.5*inch])
        result_table.setStyle(TableStyle([
            ('BACKGROUND', (0, 0), (-1, 0), colors.HexColor('#667eea')),
            ('TEXTCOLOR', (0, 0), (-1, 0), colors.whitesmoke),
            ('ALIGN', (0, 0), (-1, -1), 'LEFT'),
            ('FONTNAME', (0, 0), (-1, 0), 'Helvetica-Bold'),
            ('FONTSIZE', (0, 0), (-1, 0), 11),
            ('BOTTOMPADDING', (0, 0), (-1, 0), 12),
            ('GRID', (0, 0), (-1, -1), 1, colors.black),
            ('ROWBACKGROUNDS', (0, 1), (-1, -1), [colors.white, colors.lightgrey])
        ]))
        elements.append(result_table)
        
        # Footer
        elements.append(Spacer(1, 30))
        footer_text = "This report was generated by Crime Hotspot Prediction System using K-means clustering algorithm."
        elements.append(Paragraph(footer_text, self.styles['Italic']))
        
        doc.build(elements)
        buffer.seek(0)
        return buffer
